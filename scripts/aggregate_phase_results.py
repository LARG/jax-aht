#!/usr/bin/env python3
"""Build the aggregate phase table from the orbax training runs.

Scans results/dsse/{ppo_ego_s5,liam_ego_mlp}/ for the standard phase
labels and writes writeup/logs/phase_results_aggregate.{json,md}, the
markdown table that feeds the writeup. Held-out returns come from the
sibling heldout_eval_metrics dir when run_heldout_eval=true.
"""
import json
import sys
from pathlib import Path

REPO = Path(__file__).resolve().parent.parent
sys.path.insert(0, str(REPO))

import numpy as np  # noqa: E402

from common.save_load_utils import load_train_run  # noqa: E402

PHASES = [
    ("phase_a_ppo_ego_5seed",      "ppo_ego_s5",   "Phase A PPO ego (ndr=2)",          False),
    ("phase_a_liam_trained_5seed", "liam_ego_mlp", "Phase A LIAM trained (ndr=2)",      False),
    ("phase_a_liam_frozen_5seed",  "liam_ego_mlp", "Phase A LIAM frozen (ndr=2)",       False),
    ("phase_b_ppo_ndr1_10seed",    "ppo_ego_s5",   "Phase B PPO ego (ndr=1)",           False),
    ("phase_b_liam_ndr1_10seed",   "liam_ego_mlp", "Phase B LIAM ego (ndr=1)",          False),
    ("phase_d_ppo_pop3_heldout",   "ppo_ego_s5",   "Phase D PPO ego (ndr=2, pop=3)",    True),
    ("phase_d_liam_pop3_heldout",  "liam_ego_mlp", "Phase D LIAM ego (ndr=2, pop=3)",   True),
]


def latest_run(label_dir: Path) -> Path:
    timestamps = sorted(p for p in label_dir.iterdir() if p.is_dir())
    if not timestamps:
        raise FileNotFoundError(f"no timestamped runs under {label_dir}")
    return timestamps[-1]


def safe_extract(run_dir: Path):
    ckpt = run_dir / "ego_train_run"
    out = load_train_run(str(ckpt))
    metrics = out["metrics"]
    eli = metrics["eval_ep_last_info"]
    arr = np.asarray(eli["returned_episode_returns"])
    # arr shape: (n_seeds, n_updates, n_partners, n_eval_eps, n_agents)
    final_per_seed = arr[:, -1, :, :, :].mean(axis=(1, 2, 3))  # (n_seeds,)
    overall_mean = float(final_per_seed.mean())
    overall_std = float(final_per_seed.std(ddof=0))
    n_seeds = int(final_per_seed.size)

    heldout_mean = None
    heldout_std = None
    heldout_per_seed = None
    # When run_heldout_eval=true, evaluation/heldout_eval.py:log_heldout_metrics
    # writes a sibling orbax dir <run_dir>/heldout_eval_metrics with shape
    # (n_seeds, n_heldout_partners, n_eval_eps, n_agents).
    heldout_ckpt = run_dir / "heldout_eval_metrics"
    if heldout_ckpt.exists():
        ho = load_train_run(str(heldout_ckpt))
        if "returned_episode_returns" in ho:
            ret = np.asarray(ho["returned_episode_returns"])
            ho_per_seed = ret.mean(axis=tuple(range(1, ret.ndim)))  # (n_seeds,)
            heldout_per_seed = ho_per_seed.tolist()
            heldout_mean = float(ho_per_seed.mean())
            heldout_std = float(ho_per_seed.std(ddof=0))

    return {
        "n_seeds": n_seeds,
        "per_seed": final_per_seed.tolist(),
        "mean": overall_mean,
        "std": overall_std,
        "heldout_mean": heldout_mean,
        "heldout_std": heldout_std,
        "heldout_per_seed": heldout_per_seed,
    }


def main():
    aggregate = {}
    for label, algo_subdir, display, has_heldout in PHASES:
        path = REPO / "results" / "dsse" / algo_subdir / label
        if not path.exists():
            print(f"[skip] {display}: {path.relative_to(REPO)} missing")
            continue
        try:
            run_dir = latest_run(path)
            data = safe_extract(run_dir)
        except Exception as exc:
            print(f"[fail] {display}: {exc}")
            continue
        data["display"] = display
        data["run"] = str(run_dir.relative_to(REPO))
        aggregate[label] = data
        per_seed_str = ", ".join(f"{x:.4f}" for x in data["per_seed"])
        if data["heldout_mean"] is not None:
            ho = f"  heldout={data['heldout_mean']:.4f}+/-{data['heldout_std']:.4f}"
        else:
            ho = ""
        print(f"[ok] {display}: mean={data['mean']:.4f} std={data['std']:.4f}{ho}  per_seed=[{per_seed_str}]")

    json_path = REPO / "writeup" / "logs" / "phase_results_aggregate.json"
    json_path.parent.mkdir(parents=True, exist_ok=True)
    with json_path.open("w") as f:
        json.dump(aggregate, f, indent=2)

    md_lines = [
        "# Phase A/B/D aggregate results",
        "",
        "Generated by `scripts/aggregate_phase_results.py` from the orbax",
        "training checkpoints under `results/dsse/{ppo_ego_s5,liam_ego_mlp}/`.",
        "",
        "| Run | n seeds | trained mean +/- std | held-out mean | per-seed final |",
        "| --- | :-----: | :-------------------: | :----------: | --- |",
    ]
    for label in aggregate:
        d = aggregate[label]
        per_seed_str = ", ".join(f"{x:.3f}" for x in d["per_seed"])
        if d["heldout_mean"] is not None:
            ho = f"{d['heldout_mean']:.4f} +/- {d['heldout_std']:.4f}"
        else:
            ho = "n/a"
        md_lines.append(
            f"| {d['display']} | {d['n_seeds']} | {d['mean']:.4f} +/- {d['std']:.4f} | {ho} | [{per_seed_str}] |"
        )
    md_path = REPO / "writeup" / "logs" / "phase_results_aggregate.md"
    with md_path.open("w") as f:
        f.write("\n".join(md_lines) + "\n")

    print(f"\nwrote {json_path.relative_to(REPO)}")
    print(f"wrote {md_path.relative_to(REPO)}")


if __name__ == "__main__":
    main()
